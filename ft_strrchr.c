#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*sy;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == c)
		{
			sy = (char *)(s + i);
			return (sy);
		}
		i--;
	}
	return (0);
}
