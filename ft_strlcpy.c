#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	src_len;

	src_len = ft_strlen(src);
	if (!dst || !src)
		return (0);
	ft_memcpy(dst, src, size);
	return (size);
}
